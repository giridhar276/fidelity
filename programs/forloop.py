
# range(start,stop,step)
for val in range(1,11):
    print(val)

for val in range(10,0,-1):
    print(val)

# display even numbers
for val in range(2,11,2):
    print(val)

name = "python"
for char in name:
    print(char)
    
# display in reverse order
for char in name[::-1]:
    print(char)